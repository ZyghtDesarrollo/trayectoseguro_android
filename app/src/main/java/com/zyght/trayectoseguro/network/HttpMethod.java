/**
 * Enumeration of HTTP methods supported by Request
 */
public enum HttpMethod {
    /**
     * Use HTTP method "GET" for the request
     */
    GET,

    /**
     * Use HTTP method "POST" for the request
     */
    POST,

    /**
     * Use HTTP method "DELETE" for the request
     */
    DELETE,

    PUT,
}
